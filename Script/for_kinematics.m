function [pos] = for_kinematics(t,theta1,theta2,d3) 
% D - H Parameters
%
%  _________ ______________ ____________ _______________ _______________
%   Links   |   alpha_i    |    a_i     |       d_i     |   theta_i     |
%  _________|______________|____________|_______________|_______________|
%     1     |       0      |     230    |       0       |     theta1    |
%  _________|______________|____________|_______________|_______________|
%     2     |       0      |     170    |       0       |     theta2    |
%  _________|______________|____________|_______________|_______________|
%     3     |       0      |     0      |      -d3      |       0       |
%  _________|______________|____________|_______________|_______________|

a1 = 0.23;
a2 = 0.17;
theta1 = theta1 * pi/180;
theta2 = theta2 * pi/180;
d3 = -d3;

for i=1:length(t)
    x(i) = 0.17*cos(theta1(i) + theta2(i)) + 0.23*cos(theta1(i));
    y(i) = 0.17*sin(theta1(i) + theta2(i)) + 0.23*sin(theta1(i));
    z(i) = d3(i);
end

pos(:,1) = t';
pos(:,2) = x';
pos(:,3) = y';
pos(:,4) = z';